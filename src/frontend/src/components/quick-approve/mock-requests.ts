function chain(
  approver1: string,
  approver2?: string,
  approver3?: string,
): PendingRequest['approvalTimeline'] {
  const steps: PendingRequest['approvalTimeline'] = [
    { step: 1, approver: approver1, status: 'approved', date: '2026-04-28' },
  ];
  if (approver2) steps.push({ step: 2, approver: approver2, status: 'pending' });
  if (approver3) steps.push({ step: 3, approver: approver3, status: 'pending' });
  return steps;
}


export function buildSta181InitialValues(employee: MockEmployee): Sta181FieldValues {
  return {
    ...buildEmptyValues(),
    ...buildEmployeeDerivedValues(employee),
    ...(employee.sta181_profile_fields ?? {}),
  }
}

function buildEmptyValues(): Sta181FieldValues {
  return Object.fromEntries(
    STA181_SECTIONS.flatMap((section) => section.fields.map((field) => [field.key, ''])),
  )
}


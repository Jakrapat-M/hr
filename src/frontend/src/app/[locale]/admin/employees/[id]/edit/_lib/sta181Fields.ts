import type { MockEmployee } from '@/mocks/employees'

export interface Sta181FieldDefinition {
  readonly key: string
  readonly label: string
  readonly kind?: 'text' | 'date' | 'number' | 'checkbox'
}

export interface Sta181SectionDefinition {
  readonly id: string
  readonly process: string
  readonly section: string
  readonly fields: readonly Sta181FieldDefinition[]
}

export type Sta181FieldValues = Record<string, string>

function fields(prefix: string, labels: readonly string[]): readonly Sta181FieldDefinition[] {
  return labels.map((label) => ({
    key: `${prefix}.${label.toLowerCase().replaceAll(/[^a-z0-9]+/g, '_').replaceAll(/^_|_$/g, '')}`,
    label,
    kind: label.toLowerCase().includes('date') ? 'date' : 'text',
  }))
}


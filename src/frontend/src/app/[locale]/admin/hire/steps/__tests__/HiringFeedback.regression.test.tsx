describe('MS Teams hiring feedback regression', () => {
  it('puts DVT previous employee ID first in Step 1 and syncs it for downstream prefill', async () => {
    render(<StepIdentity />)

    const dvtInput = screen.getByLabelText('Previous Employee ID (DVT)')
    const hireDateInput = screen.getByLabelText(/วันเริ่มงาน/)

    expect(
      dvtInput.compareDocumentPosition(hireDateInput) & Node.DOCUMENT_POSITION_FOLLOWING
    ).toBeTruthy()
    expect(screen.getByText('Used to look up previous intern records and prefill matching data when available')).toBeInTheDocument()

    fireEvent.change(dvtInput, { target: { value: 'DVT-2024-0007' } })

    await waitFor(() => {
      expect(useHireWizard.getState().formData.employeeInfo.dvtPreviousId).toBe('DVT-2024-0007')
    })
  })

  it('shows employee ID as generated after submit, not pre-generated in Step 1', () => {
    render(<StepIdentity />)

    expect(screen.getByText('รหัสพนักงาน')).toBeInTheDocument()
    expect(screen.getByText(/ระบบสร้างอัตโนมัติ/)).toBeInTheDocument()
    expect(screen.getByLabelText('รหัสพนักงาน')).toHaveTextContent('สร้างหลัง Submit')
    expect(screen.getByLabelText('รหัสพนักงาน')).not.toHaveTextContent(/EMP-|^2\d{7}$/)
  })

  it('does not render removed employment-detail fields from the Teams feedback', () => {
    // dvtPreviousId and SSN remain removed from StepEmployeeInfo (moved to StepIdentity / removed entirely).
    // pfServiceDate (PVD Entry Date) was legitimately re-added in the +52 spec fields
    // as "PF Service Date / กองทุนสำรองเลี้ยงชีพ" (Area C — SF EmpEmployment.pfServiceDate).
    render(<StepEmployeeInfo />)

    expect(screen.queryByLabelText('Previous Employee ID (DVT)')).not.toBeInTheDocument()
    expect(screen.queryByLabelText(/SSN \/ National ID/)).not.toBeInTheDocument()
    // PVD Entry Date is now correctly present as pfServiceDate per Area C spec
    expect(screen.queryByLabelText('PVD Entry Date')).toBeInTheDocument()
  })

  it('validates Employee Info from visible hiring fields without the removed Employee Class UI', async () => {
    const onValidChange = vi.fn()
    render(<StepEmployeeInfo onValidChange={onValidChange} />)

    fireEvent.change(screen.getByLabelText('Employee Group'), { target: { value: '1' } })
    fireEvent.change(screen.getByLabelText('Employee Sub-Group'), { target: { value: 'U0' } })
    fireEvent.change(screen.getByLabelText(/Original Start Date/), { target: { value: '2026-05-10' } })
    fireEvent.change(screen.getByLabelText(/Seniority Start Date/), { target: { value: '2026-05-10' } })

    await waitFor(() => {
      expect(onValidChange).toHaveBeenLastCalledWith(true)
    })
    expect(useHireWizard.getState().formData.employeeInfo.employeeClass).toBeNull()
    expect(useHireWizard.getState().formData.employeeInfo.employeeGroup).toBe('1')
    expect(useHireWizard.getState().formData.employeeInfo.employeeSubGroup).toBe('U0')
  })

  it('shows Direct Manager approval requirement and notifies both Direct Manager and HRBP', () => {
    act(() => {
      const state = useHireWizard.getState()
      useHireWizard.setState({
        formData: {
          ...state.formData,
          job: {
            ...state.formData.job,
            supervisorId: '20001001',
            supervisorLabel: 'Direct Manager from FO',
          },
        },
      })
    })

    render(<ClusterReview />)

    expect(screen.getByRole('heading', { name: 'Direct Manager Approval + HRBP' })).toBeInTheDocument()
    expect(screen.getByText('Direct Manager must approve the hire; notify both Direct Manager and HRBP when submitted')).toBeInTheDocument()
    expect(screen.getByLabelText(/Direct Manager Approver/)).toHaveValue('20001001 — Direct Manager from FO')
    expect(screen.getByLabelText(/Assigned HRBP/)).toBeInTheDocument()
    expect(screen.getByLabelText('Notify both Direct Manager and HRBP via email when submitted')).toBeChecked()
    expect(screen.getByText('Direct Manager approval is required; HRBP receives the hiring notification')).toBeInTheDocument()
  })

  it('auto-fills Supervisor ID from Position FO and exposes requested working-hour fields without Pay Scale', () => {
    render(<StepJob />)

    fireEvent.click(screen.getByRole('button', { name: 'Position lookup' }))

    expect(screen.getByText('Supervisor ID')).toBeInTheDocument()
    expect(screen.getByText(/20001001 — Direct Manager from FO/)).toBeInTheDocument()

    fireEvent.click(screen.getByRole('button', { name: /Time, FTE/ }))

    expect(screen.queryByText('Compensation Organisation / โครงสร้างเงินเดือน')).not.toBeInTheDocument()
    expect(screen.queryByLabelText(/Pay Scale Type/)).not.toBeInTheDocument()
    expect(screen.queryByLabelText(/Pay Scale Area/)).not.toBeInTheDocument()
    expect(screen.queryByLabelText(/Pay Scale Group/)).not.toBeInTheDocument()
    expect(screen.queryByLabelText(/Pay Scale Level/)).not.toBeInTheDocument()
    expect(screen.getByLabelText(/Override Standard Weekly Hours/)).toBeInTheDocument()
    expect(screen.getByLabelText(/Day off Type/)).toBeInTheDocument()
  })
})
